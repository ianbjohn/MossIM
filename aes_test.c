#include <stdio.h>
#include "aes.h"

int main() {
  byte plaintext[16] = {0x01, 0x23, 0x45, 0x67, 0x89, 0xAB, 0xCD, 0xEF, 0xFE, 0xDC, 0xBA, 0x98, 0x76, 0x54, 0x32, 0x10};
  byte key[16] = {0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xAA, 0xBB, 0xCC, 0xDD, 0xEE, 0xFF};
  byte roundkeys[11][16];

  aes_encrypt(plaintext, key);

  FILE* f = fopen("ciphertext", "w");
  for (int i = 0; i < 16; i++)
    fputc(plaintext[i], f);
  fclose(f);

  aes_decrypt(plaintext, key);

  f = fopen("plaintext", "w");
  for (int i = 0; i < 16; i++)
    fputc(plaintext[i], f);
  fclose(f);

  return 0;
}
